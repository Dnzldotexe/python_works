# Counting to Twenty
# Description: Use a for loop to print the numbers from 1 to 20, inclusive

# assigning a list to numbers
numbers = list(range(1, 21))

# prints each item in the list
for number in numbers:
    print(number)

# or

# prints number from 1 to 20 using for loop
for value in range(1, 21):
    print(value)