# Odd Numbers
# Description: Create a list with only odd numbers from 1 to 20
# - Use the third argument of the range function range(<start>, <end>, <step>)
# - Use a for loop to print each item in the list

# creates a list of odd numbers from one to twenty
odd_nums = list(range(1, 21, 2))

# prints the items in the list using a for loop
for num in odd_nums:
    print(num)