# - at the end of the program, add a message of how you like those pizzas.

# Assigning pizza list to a variable pizzas
pizzas = ["Mozzarella", 
          "Hawaiian", 
          "Pepperoni",]

# prints each item in the list
for pizza in pizzas:
    print(f"\nI like a simple {pizza.title()} pizza.")

# express how much I like pizzas
print("\nI really love pizzas!")