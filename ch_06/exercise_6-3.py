# Glossary
# Description: Create Glossary using a dictionary
# - Print each word and it's meaning

# creates a empty dictionary
glossary = {}

# add information to the dictionary
glossary['Omit'] = 'skip or not use'
glossary['Increment'] = 'add something to the original value'
glossary['Loop'] = 'to repeat'
glossary['Variable'] = 'label something'
glossary['Float'] = 'a number with decimal values'

# print each word and the corresponding informations
meaning = glossary['Omit']
print(f"\nOmit means {meaning}")

meaning = glossary['Increment']
print(f"\nIncrement means {meaning}")

meaning = glossary['Loop']
print(f"\nLoop means {meaning}")

meaning = glossary['Variable']
print(f"\nVariable means {meaning}")

meaning = glossary['Float']
print(f"\nFloat means {meaning}")